package com.commonwealth.banking.data.model;

public class ATM {

    private String id;
    private String name;
    private String address;
    private Coordinate location;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getAddress() {
        return address;
    }

    public Coordinate getLocation() {
        return location;
    }
}
